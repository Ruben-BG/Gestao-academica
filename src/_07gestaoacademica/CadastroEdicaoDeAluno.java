    public void mudarFuncaoDaJanela(Boolean isTelaDeCadastro, JTablePersonalizada tabela) {

        if(isTelaDeCadastro.equals(true)) {
            
            tituloDaPaginaLabel.setText("Novo Aluno");
            telaDeCadastro = true;
            
        } else {
            
            tituloDaPaginaLabel.setText("Editar Aluno");
            
            aluno = BancoDeDados.retornarAlunos().get(tabela.getSelectedRow());
            nomeTextField.setText(aluno.getNome());
            cpfTextField.setText(aluno.getCpf());
            telefoneTextField.setText(aluno.getTelefone());
            enderecoTextField.setText(aluno.getEndereco());
            emailTextField.setText(aluno.getEmail());
            senhaPasswordField.setText(aluno.getSenha());
            
            telaDeCadastro = false;
            
        }
        
    }

    public void mouseEntrouOuSaiuDoCampo(CampoDeEscrita campoDeTexto, String textoPadrao) {

        if (campoDeTexto.hasFocus()) {

            if (campoDeTexto.getText().equals(textoPadrao)) {
                campoDeTexto.setText("");
            } else {
                campoDeTexto.setForeground(new Color(81, 81, 81));
            }

        } else {

            if (campoDeTexto.getText().equals("")) {
                campoDeTexto.setText(textoPadrao);
                campoDeTexto.setForeground(new Color(81, 81, 81));
            } else {
                campoDeTexto.setForeground(Color.BLACK);
            }

        }

    }

    public Boolean gerarPopUp() {

        Boolean campoJaExistente = false;
        Boolean cpfIsNumeric = cpfTextField.getText().chars().allMatch(Character::isDigit);
        Boolean telefoneIsNumeric = telefoneTextField.getText().chars().allMatch(Character::isDigit);
        String[] provedoresEmail = {"@hotmail.com", "@outlook.com", "@gmail.com", "@yahoo.com"};

        if ("Digite seu nome".equals(nomeTextField.getText())) {
            p = new PopUp();
            p.semNome(nomeTextField);
            return true;
        } else if (nomeTextField.getText().chars().count() < 3) {
            p = new PopUp();
            p.limiteNumero("nome", 2, nomeTextField);
            return true;
        } else if ("Digite seu CPF".equals(cpfTextField.getText())) {
            p = new PopUp();
            p.campoNaoPreenchido("CPF", cpfTextField);
            return true;
        } else if ("Digite seu email".equals(emailTextField.getText())) {
            p = new PopUp();
            p.campoNaoPreenchido("Email", emailTextField);
            return true;
        } else if ("senha".equals(String.valueOf(senhaPasswordField.getPassword()))) {
            p = new PopUp();
            p.campoNaoPreenchido("Senha", senhaPasswordField);
            return true;
        } else if (!"Digite seu telefone".equals(telefoneTextField.getText()) && !telefoneIsNumeric) {
            p = new PopUp();
            p.naoNumerico("telefone", telefoneTextField);
            return true;
        } else if (!cpfIsNumeric) {
            p = new PopUp();
            p.naoNumerico("CPF", cpfTextField);
            return true;
        } else if (!emailTextField.getText().contains(provedoresEmail[0]) && !emailTextField.getText().contains(provedoresEmail[1]) && !emailTextField.getText().contains(provedoresEmail[2]) && !emailTextField.getText().contains(provedoresEmail[3])) {
            p = new PopUp();
            p.emailErrado(emailTextField);
            return true;
        } else if (String.valueOf(senhaPasswordField.getPassword()).length() < 8) {
            p = new PopUp();
            p.limiteNumero("senha", 8, senhaPasswordField);
            return true;
        }

        return false;

    }

    //Métodos de ação
    private void acaoDoBotaoSalvar(JTablePersonalizada tabela) {

        if (telaDeCadastro && !gerarPopUp()) {

            //Aplicação de dados num novo objeto professor
            aluno = new UsuarioAluno();
            aluno.setNome(nomeTextField.getText());
            aluno.setCpf(cpfTextField.getText());
            aluno.setTelefone(telefoneTextField.getText());
            aluno.setEndereco(enderecoTextField.getText());
            aluno.setEmail(emailTextField.getText());
            aluno.setSenha(String.valueOf(senhaPasswordField.getPassword()));
            aluno.setMatricula(Usuario.getProximaMatricula());
            
            //Cadastro do novo objeto na lista
            BancoDeDados.cadastrarUsuario(aluno);
            
            //Pop-Up de confirmação da criação de novo usuário
            p = new PopUp();
            p.mensagemFinalDoCadastroDeAluno("Conta criada com sucesso!", this);
            
            //Limpeza de campos pós criação de usuário
            nomeTextField.setText("");
            cpfTextField.setText("");
            telefoneTextField.setText("");
            enderecoTextField.setText("");
            emailTextField.setText("");
            senhaPasswordField.setText("");
            
        } else if (!telaDeCadastro && !gerarPopUp()) {

            
            
        }

    }

