package _07gestaoacademica;

public class NotaPeriodo {
    
    private int periodo;
    private double nota;

    public void setNota(double nota) {
        this.nota = nota;
    }

    public void setPeriodo(int periodo) {
        this.periodo = periodo;
    }

    public double getNota() {
        return nota;
    }

    public int getPeriodo() {
        return periodo;
    }
    
}
